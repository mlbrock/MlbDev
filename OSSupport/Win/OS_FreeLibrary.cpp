// ////////////////////////////////////////////////////////////////////////////
BOOL OS_FreeLibrary(HMODULE module_handle)
{
	BOOL return_code;

 	if ((return_code = ::FreeLibrary(module_handle)) == 0)
		MLB::Utility::ThrowSystemError("Call to 'FreeLibrary()' for module "
			"handle " + MLB::Utility::ValueToStringHex(module_handle) +
			"' failed");

	return(return_code);
}
