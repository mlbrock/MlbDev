std::string &XmlStringToString(const XMLCh *in_string, std::string &out_string)
{
	class XerxescXmlStringScopedPtr {
	public:
		XerxescXmlStringScopedPtr(char *ptr)
			:ptr_(ptr)
		{
		}
		~XerxescXmlStringScopedPtr()
		{
			xercesc::XMLString::release(&ptr_);
		}
		char *get() const
		{
			return(ptr_);
		}
	private:
		char *ptr_;
	};

	XerxescXmlStringScopedPtr tmp_ptr(xercesc::XMLString::transcode(in_string));

	return(out_string.assign(tmp_ptr.get()));
}
// ////////////////////////////////////////////////////////////////////////////

// ////////////////////////////////////////////////////////////////////////////
std::string XmlStringToString(const XMLCh *in_string)
{
	std::string out_string;

	return(XmlStringToString(in_string, out_string));
}
