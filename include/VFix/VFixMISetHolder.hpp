#include <cstddef>

// ////////////////////////////////////////////////////////////////////////////

namespace MLB {

namespace VFix {

// ////////////////////////////////////////////////////////////////////////////
template <typename MISetType> class VFixMISetHolder {
public:
	typedef typename MISetType::value_type value_type;

	VFixMISetHolder()
	{
	}

	MISetType &Get()
	{
		return(the_set_);
	}

	const MISetType &Get() const
	{
		return(the_set_);
	}

	void swap(VFixMISetHolder &other)
	{
		the_set_.swap(other.the_set_);
	}

	std::size_t size() const
	{
		return(the_set_.size());
	}

private:
	MISetType the_set_;
};
// //////////////////////////////////////////////////////////////////////////// 

} // namespace VFix

} // namespace MLB

#endif // #ifdef HH__MLB__VFix__VFixMISetHolder_hpp__HH


